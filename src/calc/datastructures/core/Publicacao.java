package calc.datastructures.core;

public class Publicacao {
	private String ano;
	private String titulo;
	
	public Publicacao() {
		
	}

	public String getAno() {
		return ano;
	}

	public void setAno(String ano) {
		this.ano = ano;
	}

	public String getTitulo() {
		return titulo;
	}

	public void setTitulo(String titulo) {
		this.titulo = titulo;
	}
	
	
}
